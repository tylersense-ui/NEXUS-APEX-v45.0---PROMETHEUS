function parseArgs(args) {
    const config = {
        dryRun: false,
        force: false,
        stocksOnly: false,
        serversOnly: false,
        help: false
    };
    
    for (let i = 0; i < args.length; i++) {
        const arg = args[i];
        
        if (arg === "--dry-run") {
            config.dryRun = true;
        } else if (arg === "--force") {
            config.force = true;
        } else if (arg === "--stocks") {
            config.stocksOnly = true;
        } else if (arg === "--servers") {
            config.serversOnly = true;
        } else if (arg === "--help") {
            config.help = true;
        }
    }
    
    return config;
}

