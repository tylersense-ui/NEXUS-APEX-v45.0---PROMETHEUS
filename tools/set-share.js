function parseArgs(args) {
    const config = {
        dryRun: false,
        force: false,
        reserved: CONFIG.HACKING?.RESERVED_HOME_RAM || 32,
        help: false
    };
    
    for (let i = 0; i < args.length; i++) {
        const arg = args[i];
        
        if (arg === "--dry-run") {
            config.dryRun = true;
        } else if (arg === "--force") {
            config.force = true;
        } else if (arg === "--reserved" && i + 1 < args.length) {
            config.reserved = parseInt(args[++i]) || config.reserved;
        } else if (arg === "--help") {
            config.help = true;
        }
    }
    
    return config;
}

