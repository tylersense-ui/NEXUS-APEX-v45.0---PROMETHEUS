function parseArgs(args) {
    const config = {
        target: null,
        quiet: false,
        once: false,
        help: false
    };
    
    for (let i = 0; i < args.length; i++) {
        const arg = args[i];
        
        if (arg === "--target" && i + 1 < args.length) {
            config.target = args[++i];
        } else if (arg === "--quiet") {
            config.quiet = true;
        } else if (arg === "--once") {
            config.once = true;
        } else if (arg === "--help") {
            config.help = true;
        }
    }
    
    return config;
}

