function parseArgs(args) {
    const config = {
        url: null,
        dest: null,
        manifest: null,
        dryRun: false,
        noBackup: false,
        help: false
    };
    
    for (let i = 0; i < args.length; i++) {
        const arg = args[i];
        
        if (arg === "--url" && i + 1 < args.length) {
            config.url = args[++i];
        } else if (arg === "--dest" && i + 1 < args.length) {
            config.dest = args[++i];
        } else if (arg === "--manifest" && i + 1 < args.length) {
            config.manifest = args[++i];
        } else if (arg === "--dry-run") {
            config.dryRun = true;
        } else if (arg === "--no-backup") {
            config.noBackup = true;
        } else if (arg === "--help") {
            config.help = true;
        }
    }
    
    return config;
}

