function parseArgs(args) {
    const config = {
        sort: "level",
        filter: "all",
        compact: false,
        json: null,
        help: false
    };
    
    for (let i = 0; i < args.length; i++) {
        const arg = args[i];
        
        if (arg === "--sort" && i + 1 < args.length) {
            config.sort = args[++i];
        } else if (arg === "--filter" && i + 1 < args.length) {
            config.filter = args[++i];
        } else if (arg === "--compact") {
            config.compact = true;
        } else if (arg === "--json" && i + 1 < args.length) {
            config.json = args[++i];
        } else if (arg === "--help") {
            config.help = true;
        }
    }
    
    return config;
}

